// ── Language switching logic ────────────────────────────────
const LANG_NAMES = {
  en: 'English', 'zh-TW': '繁體中文', 'zh-CN': '简体中文',
  ja: '日本語', ko: '한국어', fr: 'Fran\u00e7ais', de: 'Deutsch',
};

function getLang() {
  const saved = localStorage.getItem('pd_lang');
  if (saved && LANGS[saved]) return saved;
  const nav = navigator.language || '';
  if (nav.startsWith('zh-TW') || nav === 'zh-Hant') return 'zh-TW';
  if (nav.startsWith('zh')) return 'zh-CN';
  if (nav.startsWith('ja')) return 'ja';
  if (nav.startsWith('ko')) return 'ko';
  if (nav.startsWith('fr')) return 'fr';
  if (nav.startsWith('de')) return 'de';
  return 'en';
}

function setLang(lang) {
  localStorage.setItem('pd_lang', lang);
  applyLang(lang);
}

// Global t(key, vars?) helper for JS-driven strings (alerts, dynamic UI).
// Falls back to English if the current language lacks the key.
// {vars} = { name: 'X' } → replaces "{name}" placeholders in the string.
function t(key, vars) {
  const lang = getLang();
  const str = (LANGS[lang] && LANGS[lang][key]) || LANGS.en[key] || key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (k in vars ? String(vars[k]) : '{' + k + '}'));
}

function applyLang(lang) {
  const t = LANGS[lang] || LANGS.en;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] === undefined) return;
    // For links/buttons: only replace text, preserve href and other attributes
    if ((el.tagName === 'A' || el.tagName === 'BUTTON') && !t[key].includes('<')) {
      el.textContent = t[key];
    } else {
      el.innerHTML = t[key];
    }
  });
  document.documentElement.lang = lang;
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = lang;
}

document.addEventListener('DOMContentLoaded', () => {
  const sel = document.getElementById('lang-select');
  if (sel) {
    Object.entries(LANG_NAMES).forEach(([code, label]) => {
      const opt = document.createElement('option');
      opt.value = code;
      opt.textContent = label;
      sel.appendChild(opt);
    });
    sel.addEventListener('change', () => setLang(sel.value));
  }
  applyLang(getLang());
});
